	private void doOneTestDecodeFinal(int[] encoded, int[] decoded, int[] fb)
			throws IOException {
		MDecoder mDecoder = createDecoder(encoded);

		Context cm = new Context(0, 0);

		for (int i = 0; i < decoded.length; i++) {
			int db;
			if (fb[i] == 0)
				db = mDecoder.decodeBin(cm);
			else
				db = mDecoder.decodeFinalBin();
			assertEquals("Bin " + i, decoded[i], db);
		}
	}

	private void doOneTestDecode(final int[] encoded, final int[] decoded,
			final int[] bypass) throws Exception {
		MDecoder mDecoder = createDecoder(encoded);

		Context cm = new Context(0, 0);

		for (int i = 0; i < decoded.length; i++) {
			int decodeBin;
			if (bypass == null || bypass[i] == 0)
				decodeBin = mDecoder.decodeBin(cm);
			else
				decodeBin = mDecoder.decodeBinBypass();

			assertEquals("Bin " + i, decoded[i], decodeBin);
		}
	}

	private MDecoder createDecoder(final int[] encoded) throws IOException {
		MDecoder mDecoder = new MDecoder(new InputStream() {
			int pos;

			public int read() throws IOException {
				if (pos >= encoded.length)
					return -1;
				return encoded[pos++];
			}
		});
		return mDecoder;
	}

	private void doOneTestEncodeFinal(int[] encoded, int[] decoded, int[] fb)
			throws IOException {
		ByteArrayOutputStream baos = new ByteArrayOutputStream();
		MEncoder encoder = new MEncoder(baos);

		Context cm = new Context(0, 0);
		for (int i = 0; i < decoded.length; i++) {
			if (fb[i] == 0)
				encoder.encodeBin(decoded[i], cm);
			else
				encoder.encodeBinFinal(decoded[i]);
		}
		encoder.finishEncoding();
		compareResult(encoded, baos);
	}

	private void doOneTestEncode(final int[] encoded, final int[] decoded,
			final int[] bypass) throws Exception {
		ByteArrayOutputStream baos = new ByteArrayOutputStream();
		MEncoder encoder = new MEncoder(baos);

		Context cm = new Context(0, 0);
		for (int i = 0; i < decoded.length; i++) {
			if (bypass == null || bypass[i] == 0)
				encoder.encodeBin(decoded[i], cm);
			else
				encoder.encodeBinBypass(decoded[i]);
		}
		encoder.encodeBinFinal(1);
		encoder.finishEncoding();
		compareResult(encoded, baos);
	}

	private void compareResult(int[] encoded, ByteArrayOutputStream baos) {
		byte[] bytes = baos.toByteArray();
		assertEquals("Lengths are not equal", encoded.length, bytes.length);
		for (int i = 0; i < encoded.length; i++) {
			assertEquals("Byte " + i + " is not equal", encoded[i],
					(bytes[i] & 0xff));
		}
	}
